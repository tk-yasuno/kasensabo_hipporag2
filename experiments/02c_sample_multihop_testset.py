"""
experiments/02c_sample_multihop_testset.py
────────────────────────────────────────────────────────────
Sample N questions from validated candidates

Usage:
    python experiments/02c_sample_multihop_testset.py --output-size 1000
    python experiments/02c_sample_multihop_testset.py --output-size 500 --seed 42
"""

from __future__ import annotations

import argparse
import json
import random
from collections import Counter
from pathlib import Path

CANDIDATES_FILE = Path(__file__).parent / "multihop_candidates_validated.jsonl"

def load_candidates(path: Path) -> list[dict]:
    """Load validated candidates"""
    candidates = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                candidates.append(json.loads(line))
    return candidates


def sample_testset(
    candidates: list[dict],
    output_size: int,
    seed: int = 42
) -> list[dict]:
    """Sample N questions from candidates"""
    
    random.seed(seed)
    
    # Filter to only validated candidates
    valid_candidates = [c for c in candidates if c.get("llm_valid", False)]
    
    if len(valid_candidates) < output_size:
        print(f"  ⚠ Warning: Only {len(valid_candidates)} valid candidates available")
        print(f"  Sampling all {len(valid_candidates)} candidates")
        sampled = valid_candidates
    else:
        sampled = random.sample(valid_candidates, output_size)
    
    # Convert to testset format
    testset = []
    for idx, cand in enumerate(sampled):
        testset.append({
            "idx": idx,
            "question": cand["question"],
            "answer": "",  # Will be generated by RAG
            "source": "multihop_generated",
            "concept_A": cand["concept_A"],
            "concept_B": cand["concept_B"],
            "concept_C": cand["concept_C"],
            "hop_count": cand["hop_count"],
            "template_id": cand["template_id"],
            "rel_types": [cand["rel_type_A"], cand["rel_type_B"]],
        })
    
    return testset


def print_statistics(testset: list[dict]):
    """Print testset statistics"""
    
    print(f"\n  Total questions: {len(testset)}")
    
    # Hop count distribution
    hop_counts = Counter(q["hop_count"] for q in testset)
    print(f"\n  Hop count distribution:")
    for hop, count in sorted(hop_counts.items()):
        print(f"    {hop}-hop: {count} ({count/len(testset)*100:.1f}%)")
    
    # Template distribution
    template_counts = Counter(q["template_id"] for q in testset)
    print(f"\n  Template distribution:")
    for tid, count in sorted(template_counts.items()):
        print(f"    T{tid+1}: {count} ({count/len(testset)*100:.1f}%)")
    
    # rel_type distribution (first rel_type only)
    rel_types = Counter(q["rel_types"][0] for q in testset if q["rel_types"])
    print(f"\n  Top 10 rel_types:")
    for rel_type, count in rel_types.most_common(10):
        print(f"    {rel_type:25s}: {count:3d} ({count/len(testset)*100:.1f}%)")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--output-size", type=int, required=True, help="Number of questions to sample")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    parser.add_argument("--output", type=str, default=None, help="Output file path (default: testset_multihop_{size}.jsonl)")
    args = parser.parse_args()
    
    print("=" * 60)
    print("  Multi-hop Testset Sampler")
    print("=" * 60)
    
    # Load candidates
    print(f"\n[Loading Candidates]")
    if not CANDIDATES_FILE.exists():
        print(f"ERROR: Candidates file not found: {CANDIDATES_FILE}")
        print(f"  Run 02b_prepare_multihop_testset.py first")
        return
    
    candidates = load_candidates(CANDIDATES_FILE)
    valid_count = sum(1 for c in candidates if c.get("llm_valid", False))
    
    print(f"  Total candidates: {len(candidates)}")
    print(f"  Valid candidates: {valid_count} ({valid_count/len(candidates)*100:.1f}%)")
    
    # Sample
    print(f"\n[Sampling {args.output_size} Questions]")
    print(f"  Seed: {args.seed}")
    
    testset = sample_testset(candidates, args.output_size, args.seed)
    
    # Save
    if args.output:
        output_file = Path(args.output)
    else:
        output_file = Path(__file__).parent / f"testset_multihop_{args.output_size}.jsonl"
    
    with open(output_file, "w", encoding="utf-8") as f:
        for rec in testset:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")
    
    print(f"\n✓ Saved testset to: {output_file}")
    
    # Statistics
    print("\n" + "=" * 60)
    print("  Statistics")
    print("=" * 60)
    
    print_statistics(testset)
    
    print("\n" + "=" * 60)
    print("  ✓ Sampling complete!")
    print("=" * 60)


if __name__ == "__main__":
    main()
